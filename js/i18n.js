let currentLang = 'en';

function detectLanguage() {
    const browserLang = navigator.language || navigator.userLanguage;
    if (browserLang.startsWith('de')) return 'de';
    if (browserLang.startsWith('hr')) return 'hr';
    return 'en';
}

function setLanguage(lang) {
    if (!translations[lang]) return;
    currentLang = lang;
    
    // Update active state on language switchers
    document.querySelectorAll('.lang-btn').forEach(btn => {
        if(btn.getAttribute('data-lang') === lang) {
            btn.classList.add('active');
        } else {
            btn.classList.remove('active');
        }
    });

    // Update texts
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang][key]) {
            el.textContent = translations[lang][key];
        }
    });

    localStorage.setItem('oaza_lang', lang);
    document.documentElement.lang = lang;
}

document.addEventListener('DOMContentLoaded', () => {
    // Determine initial language
    const savedLang = localStorage.getItem('oaza_lang');
    const initialLang = savedLang || detectLanguage();
    setLanguage(initialLang);

    // Bind language switchers
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.addEventListener('click', (e) => {
            const lang = e.target.getAttribute('data-lang');
            setLanguage(lang);
        });
    });

    // Toggle custom Mobile Nav Menu
    const mbBtn = document.querySelector('.menu-button-2');
    const ngMenu = document.querySelector('.nav-menu-wrapper-2');
    
    if(mbBtn && ngMenu) {
        mbBtn.addEventListener('click', (e) => {
            e.preventDefault();
            ngMenu.classList.toggle('mobile-nav-open');
        });
    }
});
